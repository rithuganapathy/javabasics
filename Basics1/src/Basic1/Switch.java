package Basic1;

public class Switch {
	public static void main(String[]args) {
		String day = "Thursday";
		switch(day) {
		case "Monday":
			System.out.println("Monday");
			break;
		case "Tuesday":
			System.out.println("Tuesday");
			break;
		case "Wednesday":
			System.out.println("Wednesday");
			break;
		case "Thursday":
			System.out.println("Thursday");
			break;
		}
	}

}
